package finalex;

public class Constant {
	public static void main(String [] args) {
		int num = 10;
		System.out.println(num); 
		////////
		num = 20;
		System.out.println(num);
		final int NUM = 100; // 값을 변경하지 못하는 상수
		System.out.println(NUM);
		// NUM = 200;
		// 변수에 final 을 붙여주면 상수
		
		final double PI = 3.141592; // 상수
		
		
	}
}
