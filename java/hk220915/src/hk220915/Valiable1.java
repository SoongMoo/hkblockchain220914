package hk220915;

public class Valiable1 {
	public static void main(String [] args) {
		System.out.println("hello java");
		System.out.println(10); 
		int level = 20; //level을 변수, 초기화
		// 변수는 오로지 하나의 값만 저장한다.
		// 새로운 값이 들어오면 먼저 값은 지우고 새로운 값을 저장한다.
		System.out.println(level);
		level = 30;
		System.out.println(level);
		level = 100;
		System.out.println(level);
	}
}
