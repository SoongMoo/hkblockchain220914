package hk220915;

public class Valiable2 {
	public static void main(String [] args) {
		
		int level; // 변수 선언 : 선언 할때만 자료형을 사용
		level = 10; // 변수에 값 대입 
		System.out.println(level);
		// int : 정수 : 소수점을 가지지 않은 수: 1, 20 , 100
 		level = 100;
 		System.out.println(level);
 		//level = 10.5; 
 		// 실수 : 소수점을 가진 수 
		
 		int level1 = 1000;
 		System.out.println(level1);
		
 		//level2; 변수가 선언되어 있지 않아서 오류
	}
}
